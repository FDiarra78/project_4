# Программа для поиска максимального из двух чисел

# Ввод двух чисел
a = float(input("Введите первое число: "))
b = float(input("Введите второе число: "))

# Поиск максимального числа
if a > b:
    maximum = a
elif b > a:
    maximum = b
else:
    maximum = a  # или b, так как они равны
    print("Числа равны")

# Вывод результата
print(f"Максимальное число: {maximum}")